package org.jenetics.util;

import java.util.concurrent.atomic.AtomicInteger;

import org.testng.Assert;
import org.testng.annotations.Test;

/**
 * @author <a href="mailto:franz.wilhelmstoetter@gmx.at">Franz Wilhelmstötter</a>
 * @version <em>$Date: 2013-06-21 $</em>
 */
public abstract class SeqTestBase<T> {

	protected abstract Seq<T> newSeq(final int length);

	@Test
	public void forEach() {
		final Seq<T> seq = newSeq(1000);

		final AtomicInteger counter = new AtomicInteger(0);
		seq.forEach(new Function<T, Void>() {
			@Override public Void apply(final T value) {
				counter.incrementAndGet();
				return null;
			}
		});

		Assert.assertEquals(counter.get(), seq.length());
	}

}
